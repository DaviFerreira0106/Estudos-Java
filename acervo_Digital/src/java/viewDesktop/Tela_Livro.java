    private void btncadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarActionPerformed
        // TODO add your handling code here:
        Livro objLivro = Livro.getBuilder()
        .comTitulo(txttitulo.getText())
        .comAutor(txtautor.getText())
        .comGenero(txtgenero.getText())
        .comEditora(txteditora.getText())
        .comIdioma(txtidioma.getText())
        .comNumeroPaginas(Integer.parseInt(txtnumeropagina.getText()))
        .comAnoPublicacao(Integer.parseInt(txtanopublicacao.getText()))
        .comClassificacaoIndicativa(txtclassificacaoindicativa.getText())
        .comPreco(Double.parseDouble(txtpreco.getText()))
        .constroi();

        ControleFacadeDesktop cfd = new ControleFacadeDesktop();
        lblmensagem.setText(cfd.acionarCadastrar(objLivro));
    }//GEN-LAST:event_btncadastrarActionPerformed

