    private void btnconsultarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarIdActionPerformed
        // TODO add your handling code here:
        Livro objLivro = Livro.getBuilder().comId(Integer.parseInt(txtid.getText())).constroi();
        
        ControleFacadeDesktop cfd = new ControleFacadeDesktop();
        Livro objLivro2 = cfd.acionarConsultarPorId(objLivro);
        
        labelautor.setText(objLivro2.getAutor());
        labelclassificacao.setText(objLivro2.getClassificacaoIndicativa());
        labeleditora.setText(objLivro2.getEditora());
        labelgenero.setText(objLivro2.getGenero());
        labelidioma.setText(objLivro2.getIdioma());
        labelautor.setText(objLivro2.getAutor());
        labelautor.setText(objLivro2.getAutor());
        labeltitulo.setText(objLivro2.getTitulo());
    }//GEN-LAST:event_btnconsultarIdActionPerformed

